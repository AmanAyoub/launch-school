// We can define some simple functions that does combination:
function devide(num1, num2) {
	return num1 / num2
}
console.log(devide(25, 5)); // returns 5
console.log(devide(100, 5)); // returns 20

function subtract(num1, num2) {
	return num1 - num2
}
console.log(subtract(12, 6)); // returns 6
console.log(subtract(25, 5)); // returns 20
