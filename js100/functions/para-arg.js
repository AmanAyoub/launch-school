// A parameter is one of the variables in a function. 
// And when a method is called, the arguments are the data you pass into the method's parameters.

// Create a function that says hello
function sayHi(name) { /* name is a parameter or an empty variable it takes value as an argument 
	while invoking the function*/
	console.log(`Hello, ${name}`);
}

sayHi(Aman); // returns Hello, Aman


