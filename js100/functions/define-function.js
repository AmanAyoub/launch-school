// There are 3 ways to define a function.

// Function Declaration:
function greet() {
	console.log(`Hello, good morning`);
}
greet(); // returns Hello, good morning


// Function Expression:
let sayHi = function () {
	console.log('Hi');
}
sayHi(); // returns Hi


// Arrow Function:
let sayBye = () => {
	console.log('Bye');
}
sayBye(); // returns Bye

