// Write code that logs the numbers from 10 to 1 in decreasing order, and then logs 'Launch!'.

for (let i = 10; i > 0; i--) {
	console.log(i);
}
console.log('Launch!');

// The above code decreases 1 from i local variable in each iteration until i is greater than 0.
// logs:
// 10
// 9
// 8
// 7...
// Launch!
