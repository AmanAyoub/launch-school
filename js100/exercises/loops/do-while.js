// What's the difference between do while and while loop?

// The while loop loops through a block of code as long as a specified condition is true:
let counter = 0;

while (counter > 0) {
  console.log('Woooot!');
  counter -= 1;
}


// The do...while statements defines a code block to be executed once, 
// and then repeat as long as the condition is true.
// The do...while is used when you want to run a code block at least one time:
let counter = 0;

do {
  console.log('Woooot!');
  counter -= 1;
} while (counter > 0);























