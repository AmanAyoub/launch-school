// Write the code necessary to retrieve the value of the
// courses property of our student object.

let student = {
  name: 'Carmen',
  age: 14,
  grade: 10,
  courses: ['biology', 'algebra', 'composition', 'ceramics'],
  gpa: 3.75,
};

// We can access a specific property of an object
// using dot notation or bracket notation.

// Dot notation:
console.log(student.courses); // [ 'biology', 'algebra', 'composition', 'ceramics' ]

// Bracket notation:
console.log(student.courses); // [ 'biology', 'algebra', 'composition', 'ceramics' ]