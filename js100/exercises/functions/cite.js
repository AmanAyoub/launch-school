// Implement a function cite that takes two string arguments: the author of the quote and what they said. 
// It then logs the quote to the console.

function cite(author, quote) {
        console.log(`${author} said: ${quote}`);
}

let brendanEichQuote = cite('Brendan Eich', 'Always bet on JavaScript.');
console.log(brendanEichQuote);

// logs:
// Brendan Eich said: Always bet on JavaScript.

